    id bigint PRIMARY KEY GENERATED always as identity unique ,
    thickness numeric(5,2) not null,
    material_id bigint not null ,
    voltage_id bigint not null,
    material_lead_equivalent_id bigint not null,
    constraint fk_material_id FOREIGN KEY (material_id) references material(id),
    constraint fk_voltage_id FOREIGN KEY (voltage_id) references voltage(id),
    constraint fk_material_lead_equivalent_id FOREIGN KEY (material_lead_equivalent_id) references material_lead_equivalent(id)
);


